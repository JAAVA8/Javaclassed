class Relation1{

 public static void main(String[] args)
 {
int a=1;
int b=1;
//OR operator
System.out.println(a==1||b==2);
System.out.println(a==2||b==1);
System.out.println(a==1||b==1);
System.out.println(a==2||b==2);
//AND Operator
System.out.println(a==1&&b==2);
System.out.println(a==2&&b==1);
System.out.println(a==2&&b==2);
System.out.println(a==1&&b==1);
}
}